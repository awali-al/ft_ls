#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			jumper;
	unsigned char	*t;

	jumper = 0;
	t = s;
	while (jumper < n)
	{
		t[jumper] = '\0';
		jumper++;
	}
}
