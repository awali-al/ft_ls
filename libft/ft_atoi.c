int		ft_atoi(const char *str)
{
	int			i;
	long long	res;

	res = 0;
	i = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 12) || *str == '\t'
			|| *str == '\r' || *str == '\f' || *str == '\v')
		str++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (str[0] == '-')
		res = res * (-1);
	return ((int)res);
}
